package cursojava.executavel;

public class OperacoesRelacionais {
	public static void main(String[] args) {
		
		/* == - Igual
		 * != - diferente
		 * <> - maior ou menor
		 * <  - menor
		 * >  - maior
		 * >= - maior igual
		 * <= - menor igual
		 * 
		 * 
		 * 
		 **/
			
		int nota1 = 91;
		int nota2 = 90;
		
		if (nota1 == nota2) {
			System.out.println("As notas são iguais");
		}else {
			System.out.println("As notas são diferentes");
		}
	}
}
