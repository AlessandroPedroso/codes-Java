package cursojava.executavel;

public class OperacaoCasasDecimais {
	public static void main(String[] args) {
		
		double nota1 = 90;
		double nota2 = 80;
		double nota3 = 55.5;
		double nota4 = 40;
		
		double mediaFinal = (nota1 + nota2 + nota3 + nota4) / 4;
		
		System.out.println("Média das notas: " + mediaFinal);
	}
}
